create or replace function public.open_event_trivia_round(p_question_key text,p_duration_seconds integer default 25)
returns public.event_trivia_rounds language plpgsql security definer set search_path=public as $$
declare v_room public.event_rooms;v_question public.event_trivia_questions;v_round public.event_trivia_rounds;
begin
 if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido'; end if;
 select * into v_room from public.event_rooms where slug='inauguracion-lihen';
 select * into v_question from public.event_trivia_questions where question_key=p_question_key and is_active=true;
 if v_question.id is null then raise exception 'Pregunta no encontrada'; end if;
 update public.event_trivia_rounds set status='closed',closed_at=now() where room_id=v_room.id and status='open';
 p_duration_seconds:=greatest(10,least(coalesce(p_duration_seconds,25),90));
 insert into public.event_trivia_rounds(room_id,question_id,status,opened_at,closes_at,duration_seconds)
 values(v_room.id,v_question.id,'open',now(),now()+make_interval(secs=>p_duration_seconds),p_duration_seconds) returning * into v_round;
 update public.event_rooms set active_activity='Trivia LIHEN',updated_at=now() where id=v_room.id;
 return v_round;
end;$$;

create or replace function public.close_event_trivia_round() returns public.event_trivia_rounds
language plpgsql security definer set search_path=public as $$
declare v_round public.event_trivia_rounds;begin
 if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido'; end if;
 update public.event_trivia_rounds set status='closed',closed_at=now() where id=(select id from public.event_trivia_rounds order by created_at desc limit 1) returning * into v_round;return v_round;end;$$;

create or replace function public.reset_event_trivia_round() returns boolean
language plpgsql security definer set search_path=public as $$begin
 if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido'; end if;
 delete from public.event_trivia_rounds where room_id=(select id from public.event_rooms where slug='inauguracion-lihen');return true;end;$$;

create or replace function public.submit_event_trivia_answer(p_public_id uuid,p_access_code text,p_round_id bigint,p_selected_option integer)
returns jsonb language plpgsql security definer set search_path=public as $$
declare v_round public.event_trivia_rounds;v_participant public.event_participants;v_correct integer;v_is_correct boolean;v_response public.event_trivia_responses;
begin
 select * into v_round from public.event_trivia_rounds where id=p_round_id for update;
 if v_round.id is null or v_round.status<>'open' or now()>v_round.closes_at then raise exception 'La ronda no está abierta'; end if;
 select ep.* into v_participant from public.event_participants ep join public.event_rooms er on er.active_participant_id=ep.id
 where ep.public_id=p_public_id and ep.access_code=p_access_code and er.id=v_round.room_id and ep.registration_status='active';
 if v_participant.id is null then raise exception 'Solo el participante con el turno activo puede responder'; end if;
 select correct_option into v_correct from public.event_trivia_answers where question_id=v_round.question_id;
 v_is_correct:=p_selected_option=v_correct;
 insert into public.event_trivia_responses(round_id,participant_id,selected_option,is_correct,response_ms)
 values(v_round.id,v_participant.id,p_selected_option,v_is_correct,greatest(0,floor(extract(epoch from(now()-v_round.opened_at))*1000)::integer))
 returning * into v_response;
 if v_is_correct and v_round.winner_participant_id is null then
  update public.event_trivia_rounds set winner_participant_id=v_participant.id,winner_display_name=v_participant.display_name,status='closed',closed_at=now() where id=v_round.id;
 end if;
 return jsonb_build_object('ok',true,'is_correct',v_is_correct,'response_ms',v_response.response_ms);
exception when unique_violation then raise exception 'Ya respondiste esta ronda';end;$$;

revoke all on function public.open_event_trivia_round(text,integer) from public;revoke all on function public.close_event_trivia_round() from public;revoke all on function public.reset_event_trivia_round() from public;revoke all on function public.submit_event_trivia_answer(uuid,text,bigint,integer) from public;
