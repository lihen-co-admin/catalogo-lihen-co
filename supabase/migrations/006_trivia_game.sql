-- ETAPA 12: Trivia LIHEN con pregunta pública, respuesta privada y validación en servidor.

create table if not exists public.event_trivia_questions (
  id bigint generated by default as identity primary key,
  question_key text unique not null,
  question_text text not null,
  options jsonb not null check (jsonb_typeof(options)='array' and jsonb_array_length(options) between 2 and 6),
  position integer not null default 1,
  is_active boolean not null default true
);

create table if not exists public.event_trivia_answers (
  question_id bigint primary key references public.event_trivia_questions(id) on delete cascade,
  correct_option integer not null check (correct_option between 0 and 5)
);

create table if not exists public.event_trivia_rounds (
  id bigint generated by default as identity primary key,
  room_id uuid not null references public.event_rooms(id) on delete cascade,
  question_id bigint not null references public.event_trivia_questions(id),
  status text not null default 'waiting' check (status in ('waiting','open','closed')),
  opened_at timestamptz,
  closes_at timestamptz,
  duration_seconds integer not null default 25 check (duration_seconds between 10 and 90),
  winner_participant_id bigint references public.event_participants(id) on delete set null,
  winner_display_name text,
  created_at timestamptz not null default now(),
  closed_at timestamptz
);

create table if not exists public.event_trivia_responses (
  id bigint generated by default as identity primary key,
  round_id bigint not null references public.event_trivia_rounds(id) on delete cascade,
  participant_id bigint not null references public.event_participants(id) on delete cascade,
  selected_option integer not null check (selected_option between 0 and 5),
  is_correct boolean not null,
  response_ms integer,
  answered_at timestamptz not null default now(),
  unique(round_id,participant_id)
);

alter table public.event_trivia_questions enable row level security;
alter table public.event_trivia_answers enable row level security;
alter table public.event_trivia_rounds enable row level security;
alter table public.event_trivia_responses enable row level security;

create or replace view public.event_trivia_questions_public as
select id,question_key,question_text,options,position,is_active from public.event_trivia_questions;

create or replace view public.event_trivia_rounds_public as
select r.id,er.slug as room_slug,r.status,r.opened_at,r.closes_at,r.duration_seconds,r.winner_display_name,r.created_at,r.closed_at,
       q.question_key,q.question_text,q.options
from public.event_trivia_rounds r
join public.event_rooms er on er.id=r.room_id
join public.event_trivia_questions q on q.id=r.question_id;

create or replace view public.event_trivia_responses_admin as
select tr.id,tr.round_id,tr.selected_option,tr.is_correct,tr.response_ms,tr.answered_at,ep.display_name,ep.modality
from public.event_trivia_responses tr join public.event_participants ep on ep.id=tr.participant_id;

grant select on public.event_trivia_questions_public to anon,authenticated;
grant select on public.event_trivia_rounds_public to anon,authenticated;
grant select on public.event_trivia_responses_admin to authenticated;

create policy "admins manage trivia questions" on public.event_trivia_questions for all to authenticated
using (public.is_active_event_admin()) with check (public.is_active_event_admin());
create policy "admins read trivia answers" on public.event_trivia_answers for select to authenticated using (public.is_active_event_admin());
create policy "public reads trivia rounds" on public.event_trivia_rounds for select to anon,authenticated using (true);
create policy "admins manage trivia rounds" on public.event_trivia_rounds for all to authenticated
using (public.is_active_event_admin()) with check (public.is_active_event_admin());
create policy "admins read trivia responses" on public.event_trivia_responses for select to authenticated using (public.is_active_event_admin());

