-- ETAPA 14: Vota tu favorito con participación simultánea y ranking público en tiempo real.
create table if not exists public.event_vote_polls(
 id bigint generated by default as identity primary key,
 poll_key text unique not null,
 question_text text not null,
 position integer not null default 1,
 is_active boolean not null default true
);
create table if not exists public.event_vote_options(
 id bigint generated by default as identity primary key,
 poll_id bigint not null references public.event_vote_polls(id) on delete cascade,
 option_key text not null,
 option_label text not null,
 option_description text,
 position integer not null default 1,
 unique(poll_id,option_key)
);
create table if not exists public.event_vote_rounds(
 id bigint generated by default as identity primary key,
 room_id uuid not null references public.event_rooms(id) on delete cascade,
 poll_id bigint not null references public.event_vote_polls(id),
 status text not null default 'waiting' check(status in('waiting','open','closed')),
 opened_at timestamptz,
 closes_at timestamptz,
 duration_seconds integer not null default 45 check(duration_seconds between 10 and 300),
 created_at timestamptz not null default now(),
 closed_at timestamptz
);
create table if not exists public.event_votes(
 id bigint generated by default as identity primary key,
 round_id bigint not null references public.event_vote_rounds(id) on delete cascade,
 participant_id bigint not null references public.event_participants(id) on delete cascade,
 option_id bigint not null references public.event_vote_options(id),
 voted_at timestamptz not null default now(),
 unique(round_id,participant_id)
);
alter table public.event_vote_polls enable row level security;
alter table public.event_vote_options enable row level security;
alter table public.event_vote_rounds enable row level security;
alter table public.event_votes enable row level security;

create or replace view public.event_vote_rounds_public as
select r.id,er.slug room_slug,r.status,r.opened_at,r.closes_at,r.duration_seconds,r.created_at,r.closed_at,p.poll_key,p.question_text,
 coalesce(jsonb_agg(jsonb_build_object('key',o.option_key,'label',o.option_label,'description',o.option_description,'position',o.position) order by o.position) filter(where o.id is not null),'[]'::jsonb) options
from public.event_vote_rounds r
join public.event_rooms er on er.id=r.room_id
join public.event_vote_polls p on p.id=r.poll_id
left join public.event_vote_options o on o.poll_id=p.id
group by r.id,er.slug,p.id;

create or replace view public.event_vote_ranking_public as
select r.id round_id,o.option_key,o.option_label,o.position option_position,count(v.id)::integer vote_count
from public.event_vote_rounds r
join public.event_vote_options o on o.poll_id=r.poll_id
left join public.event_votes v on v.round_id=r.id and v.option_id=o.id
group by r.id,o.id;

create or replace view public.event_votes_admin as
select v.id,v.round_id,v.voted_at,p.display_name,p.modality,o.option_key,o.option_label
from public.event_votes v
join public.event_participants p on p.id=v.participant_id
join public.event_vote_options o on o.id=v.option_id;

grant select on public.event_vote_rounds_public to anon,authenticated;
grant select on public.event_vote_ranking_public to anon,authenticated;
grant select on public.event_votes_admin to authenticated;
create policy "public reads vote polls" on public.event_vote_polls for select to anon,authenticated using(true);
create policy "admins manage vote polls" on public.event_vote_polls for all to authenticated using(public.is_active_event_admin()) with check(public.is_active_event_admin());
create policy "public reads vote options" on public.event_vote_options for select to anon,authenticated using(true);
create policy "admins manage vote options" on public.event_vote_options for all to authenticated using(public.is_active_event_admin()) with check(public.is_active_event_admin());
create policy "public reads vote rounds" on public.event_vote_rounds for select to anon,authenticated using(true);
create policy "admins manage vote rounds" on public.event_vote_rounds for all to authenticated using(public.is_active_event_admin()) with check(public.is_active_event_admin());
create policy "admins read votes" on public.event_votes for select to authenticated using(public.is_active_event_admin());

