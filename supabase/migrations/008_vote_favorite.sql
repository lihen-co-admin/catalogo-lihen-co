create or replace function public.open_event_vote_round(p_poll_key text,p_duration_seconds integer default 45)
returns public.event_vote_rounds language plpgsql security definer set search_path=public as $$
declare v_room public.event_rooms;v_poll public.event_vote_polls;v_round public.event_vote_rounds;
begin
 if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido';end if;
 select * into v_room from public.event_rooms where slug='inauguracion-lihen';
 select * into v_poll from public.event_vote_polls where poll_key=p_poll_key and is_active=true;
 if v_poll.id is null then raise exception 'Votación no encontrada';end if;
 update public.event_vote_rounds set status='closed',closed_at=now() where room_id=v_room.id and status='open';
 p_duration_seconds:=greatest(10,least(coalesce(p_duration_seconds,45),300));
 insert into public.event_vote_rounds(room_id,poll_id,status,opened_at,closes_at,duration_seconds) values(v_room.id,v_poll.id,'open',now(),now()+make_interval(secs=>p_duration_seconds),p_duration_seconds) returning * into v_round;
 update public.event_rooms set active_activity='Vota tu favorito',updated_at=now() where id=v_room.id;
 return v_round;
end;$$;

create or replace function public.close_event_vote_round() returns public.event_vote_rounds language plpgsql security definer set search_path=public as $$
declare v_round public.event_vote_rounds;begin
 if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido';end if;
 update public.event_vote_rounds set status='closed',closed_at=now() where id=(select id from public.event_vote_rounds order by created_at desc limit 1) returning * into v_round;
 return v_round;
end;$$;

create or replace function public.reset_event_vote_round() returns boolean language plpgsql security definer set search_path=public as $$
begin
 if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido';end if;
 delete from public.event_vote_rounds where room_id=(select id from public.event_rooms where slug='inauguracion-lihen');
 return true;
end;$$;

create or replace function public.submit_event_vote(p_public_id uuid,p_access_code text,p_round_id bigint,p_option_key text)
returns jsonb language plpgsql security definer set search_path=public as $$
declare v_round public.event_vote_rounds;v_participant public.event_participants;v_option public.event_vote_options;v_vote public.event_votes;
begin
 select * into v_round from public.event_vote_rounds where id=p_round_id for update;
 if v_round.id is null or v_round.status<>'open' or now()>v_round.closes_at then raise exception 'La votación no está abierta';end if;
 select * into v_participant from public.event_participants where public_id=p_public_id and access_code=p_access_code and room_id=v_round.room_id and registration_status not in('disabled','left');
 if v_participant.id is null then raise exception 'El registro del participante no es válido';end if;
 select * into v_option from public.event_vote_options where poll_id=v_round.poll_id and option_key=p_option_key;
 if v_option.id is null then raise exception 'La opción seleccionada no es válida';end if;
 insert into public.event_votes(round_id,participant_id,option_id) values(v_round.id,v_participant.id,v_option.id) returning * into v_vote;
 return jsonb_build_object('ok',true,'vote_id',v_vote.id);
exception when unique_violation then raise exception 'Ya registraste tu voto en esta ronda';end;$$;

