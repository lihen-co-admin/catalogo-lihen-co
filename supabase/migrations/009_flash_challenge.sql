create or replace function public.open_event_flash_round(p_challenge_key text,p_duration_seconds integer default 30)
returns public.event_flash_rounds language plpgsql security definer set search_path=public as $$
declare v_room public.event_rooms;v_challenge public.event_flash_challenges;v_round public.event_flash_rounds;
begin
 if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido';end if;
 select * into v_room from public.event_rooms where slug='inauguracion-lihen' for update;
 if v_room.active_participant_id is null then raise exception 'Primero debes activar el turno de un participante';end if;
 select * into v_challenge from public.event_flash_challenges where challenge_key=p_challenge_key and is_active=true;
 if v_challenge.id is null then raise exception 'Reto no encontrado';end if;
 update public.event_flash_rounds set status='closed',closed_at=now() where room_id=v_room.id and status='open';
 p_duration_seconds:=greatest(10,least(coalesce(p_duration_seconds,30),180));
 insert into public.event_flash_rounds(room_id,challenge_id,active_participant_id,status,opened_at,closes_at,duration_seconds)
 values(v_room.id,v_challenge.id,v_room.active_participant_id,'open',now(),now()+make_interval(secs=>p_duration_seconds),p_duration_seconds) returning * into v_round;
 update public.event_rooms set active_activity='Reto relámpago',updated_at=now() where id=v_room.id;
 return v_round;
end;$$;
create or replace function public.close_event_flash_round() returns public.event_flash_rounds language plpgsql security definer set search_path=public as $$
declare v_round public.event_flash_rounds;begin if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido';end if;update public.event_flash_rounds set status='closed',closed_at=now() where id=(select id from public.event_flash_rounds order by created_at desc limit 1) returning * into v_round;return v_round;end;$$;
create or replace function public.reset_event_flash_round() returns boolean language plpgsql security definer set search_path=public as $$begin if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido';end if;delete from public.event_flash_rounds where room_id=(select id from public.event_rooms where slug='inauguracion-lihen');return true;end;$$;
create or replace function public.submit_event_flash_completion(p_public_id uuid,p_access_code text,p_round_id bigint) returns jsonb language plpgsql security definer set search_path=public as $$
declare v_round public.event_flash_rounds;v_participant public.event_participants;v_submission public.event_flash_submissions;
begin
 select * into v_round from public.event_flash_rounds where id=p_round_id for update;
 if v_round.id is null or v_round.status<>'open' or now()>v_round.closes_at then raise exception 'La ronda no está abierta';end if;
 select * into v_participant from public.event_participants where public_id=p_public_id and access_code=p_access_code and id=v_round.active_participant_id and registration_status='active';
 if v_participant.id is null then raise exception 'Solo el participante con el turno activo puede registrar el reto';end if;
 insert into public.event_flash_submissions(round_id,participant_id) values(v_round.id,v_participant.id) returning * into v_submission;
 return jsonb_build_object('ok',true,'submission_id',v_submission.id,'review_status','pending');
exception when unique_violation then raise exception 'Ya reportaste este reto';end;$$;
create or replace function public.review_event_flash_submission(p_submission_id bigint,p_decision text,p_note text default '') returns jsonb language plpgsql security definer set search_path=public as $$
declare v_submission public.event_flash_submissions;v_participant public.event_participants;
begin
 if not public.is_active_event_admin() then raise exception 'Acceso administrativo requerido';end if;
 if p_decision not in('approved','rejected') then raise exception 'Decisión no válida';end if;
 select * into v_submission from public.event_flash_submissions where id=p_submission_id for update;
 if v_submission.id is null then raise exception 'Reporte no encontrado';end if;
 select * into v_participant from public.event_participants where id=v_submission.participant_id;
 update public.event_flash_submissions set review_status=p_decision,review_note=nullif(trim(p_note),''),reviewed_at=now(),reviewed_by=auth.uid() where id=p_submission_id;
 update public.event_flash_rounds set review_status=p_decision,reviewed_participant_id=v_participant.id,reviewed_display_name=v_participant.display_name,reviewed_at=now(),status='closed',closed_at=now() where id=v_submission.round_id;
 if p_decision='approved' then update public.event_participants set registration_status='winner' where id=v_participant.id;end if;
 return jsonb_build_object('ok',true,'decision',p_decision,'participant',v_participant.display_name);
end;$$;
