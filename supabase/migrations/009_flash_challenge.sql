-- ETAPA 15: Reto relámpago con turno individual y validación administrativa.
create table if not exists public.event_flash_challenges(
 id bigint generated by default as identity primary key,
 challenge_key text unique not null,
 title text not null,
 instruction_text text not null,
 completion_label text not null default 'Ya completé el reto',
 position integer not null default 1,
 is_active boolean not null default true
);
create table if not exists public.event_flash_rounds(
 id bigint generated by default as identity primary key,
 room_id uuid not null references public.event_rooms(id) on delete cascade,
 challenge_id bigint not null references public.event_flash_challenges(id),
 active_participant_id bigint references public.event_participants(id) on delete set null,
 status text not null default 'waiting' check(status in('waiting','open','closed')),
 opened_at timestamptz, closes_at timestamptz,
 duration_seconds integer not null default 30 check(duration_seconds between 10 and 180),
 review_status text not null default 'pending' check(review_status in('pending','approved','rejected')),
 reviewed_participant_id bigint references public.event_participants(id) on delete set null,
 reviewed_display_name text,
 reviewed_at timestamptz,
 created_at timestamptz not null default now(), closed_at timestamptz
);
create table if not exists public.event_flash_submissions(
 id bigint generated by default as identity primary key,
 round_id bigint not null references public.event_flash_rounds(id) on delete cascade,
 participant_id bigint not null references public.event_participants(id) on delete cascade,
 submitted_at timestamptz not null default now(),
 review_status text not null default 'pending' check(review_status in('pending','approved','rejected')),
 review_note text,
 reviewed_at timestamptz,
 reviewed_by uuid references auth.users(id) on delete set null,
 unique(round_id,participant_id)
);
alter table public.event_flash_challenges enable row level security;
alter table public.event_flash_rounds enable row level security;
alter table public.event_flash_submissions enable row level security;

create or replace view public.event_flash_rounds_public as
select r.id,er.slug room_slug,r.status,r.opened_at,r.closes_at,r.duration_seconds,r.review_status,
 r.reviewed_display_name,r.created_at,r.closed_at,c.challenge_key,c.title,c.instruction_text,c.completion_label,
 p.public_id active_participant_public_id,p.display_name active_display_name,
 exists(select 1 from public.event_flash_submissions s where s.round_id=r.id) has_submission
from public.event_flash_rounds r
join public.event_rooms er on er.id=r.room_id
join public.event_flash_challenges c on c.id=r.challenge_id
left join public.event_participants p on p.id=r.active_participant_id;
create or replace view public.event_flash_submissions_admin as
select s.id,s.round_id,s.submitted_at,s.review_status,s.review_note,s.reviewed_at,p.display_name,p.modality
from public.event_flash_submissions s join public.event_participants p on p.id=s.participant_id;
grant select on public.event_flash_rounds_public to anon,authenticated;
grant select on public.event_flash_submissions_admin to authenticated;

create policy "admins manage flash challenges" on public.event_flash_challenges for all to authenticated using(public.is_active_event_admin()) with check(public.is_active_event_admin());
create policy "public reads flash rounds" on public.event_flash_rounds for select to anon,authenticated using(true);
create policy "admins manage flash rounds" on public.event_flash_rounds for all to authenticated using(public.is_active_event_admin()) with check(public.is_active_event_admin());
create policy "admins read flash submissions" on public.event_flash_submissions for select to authenticated using(public.is_active_event_admin());

